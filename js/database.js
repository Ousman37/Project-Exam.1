var showItemCount = 10;
var showViewMoreBtn = false;
if (document.getElementById("post-data-section")) {
  addData();
}
function addData() {
  var dataStr = "";
  postDataArray.forEach((element, index) => {
    if (index < showItemCount) {
      dataStr += `<figure>
<h3>${element.title}</h3>
<img src="${element.imgUrl}"  alt="food" display: grid; height="210" width="300"   transform: scale(1.05);  />
</figure>
<p>${element.desc_first}
<a style="display:block;padding:3px 5px 1px 10px; cursor: pointer; background: #a52a2a;width:80px;color:#ffffff;" href="./post_specific.html?id=${element.id}">Read </a>
</p>`;
    }
    if (index >= showItemCount) {
      showViewMoreBtn = true;
    } else {
      showViewMoreBtn = false;
    }
  });

  document.getElementById("post-data-section").innerHTML = dataStr;
  if (showViewMoreBtn) {
    document.getElementById("didi_view_more_btn").style.display = "block";
  } else {
    document.getElementById("didi_view_more_btn").style.display = "none";
  }
  //<blockquote class="quote">
}
function viewMoreBtn() {
  showItemCount += 10;
  addData();
}
